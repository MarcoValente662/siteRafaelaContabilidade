export function formatDate(dateStr, lang) {
  const date = new Date(dateStr);
  const locale = lang === "en" ? "en-GB" : lang === "es" ? "es-ES" : "pt-PT";
  return date.toLocaleDateString(locale, {
    year: "numeric",
    month: "long",
    day: "numeric",
  });
}
